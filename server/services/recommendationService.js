const TEMPLATES = {
  wrong_pitch: [
    'Практикуйте хроматическую гамму в медленном темпе с тюнером.',
    'Удерживайте каждую ноту 3 секунды, контролируя чистоту интонации.',
    'Повторите фрагмент с акцентом на интервалы секунда и терция.',
  ],
  missing_note: [
    'Разберите произведение по тактам и сыграйте каждый такт отдельно.',
    'Проверьте, не пропускаете ли вы ноты в быстрых пассажах.',
  ],
  early: [
    'Используйте метроном на темпе 60 ударов в минуту.',
    'Отработайте ритмический рисунок хлопками без инструмента.',
  ],
  late: [
    'Снизьте темп и постепенно увеличивайте скорость с метрономом.',
    'Сосредоточьтесь на точном попадании в начало каждой ноты.',
  ],
  articulation: [
    'Практикуйте упражнения на staccato и legato попеременно.',
    'Следите за одинаковой длительностью коротких нот.',
  ],
  duration: [
    'Сравните длительность нот с эталоном и укорачивайте или удлиняйте звучание.',
  ],
};

const DEFAULT = [
  'Продолжайте регулярные занятия для закрепления навыков.',
  'Запишите исполнение ещё раз через несколько дней для сравнения прогресса.',
];

function generateRecommendations(errors, compositionTitle = '') {
  const texts = new Set();
  const types = [...new Set(errors.map((e) => e.type))];

  types.forEach((type) => {
    const list = TEMPLATES[type] || [];
    list.slice(0, 2).forEach((text) => {
      texts.add(compositionTitle ? text.replace('произведение', `«${compositionTitle}»`) : text);
    });
  });

  if (texts.size === 0) {
    DEFAULT.forEach((t) => texts.add(t));
  }

  return [...texts].slice(0, 5);
}

module.exports = { generateRecommendations };
