const DEFAULT = [
  'Продолжайте регулярные занятия для закрепления навыков.',
  'Запишите исполнение ещё раз через несколько дней для сравнения прогресса.',
];

function generateRecommendations(errors, compositionTitle = '') {
  const texts = new Set();
  const types = [...new Set(errors.map((e) => e.type))];

  types.forEach((type) => {
    const list = TEMPLATES[type] || [];
    list.slice(0, 2).forEach((text) => {
      texts.add(compositionTitle ? text.replace('произведение', `«${compositionTitle}»`) : text);
    });
  });

  if (texts.size === 0) {
    DEFAULT.forEach((t) => texts.add(t));
  }

  return [...texts].slice(0, 5);
}

module.exports = { generateRecommendations };
